/**
* print_last_digit - The primary function being carried out
* @n: the integer that is parsed for last number
* Description: Parses 'n' and returns 'val'
* a blank line
* Return: returns @val
*/
int print_last_digit(int n)
{
int val;
val = (n % 10);
_putchar(val);
return (val);
}
